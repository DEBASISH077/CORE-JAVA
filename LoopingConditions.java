public class LoopingConditions {
   
    public static void main(String[] args) {
        
        for(int i=0;i<=5;i++){
            System.out.println("Hello");
        }
        int m=2;
        for(System.out.println("Hi");m<20;m+=2){
            System.out.println("Hii");
        }

}
}